void main(List<String> args) {
  // AULA_04 Estruturas de repetição.

  // tabuada do 5
  print('Tabuada do 5');
  for (int i = 1; i <= 10; i++) {
    print(5 * i);
  }

  // loop
  print('Loop de repetiçaõ');
  int contador = 10;
  while (contador != 1) {
    contador = contador - 1;
    print(contador);
  }
}
